    OVERLAY_IP=$(echo "$ENROLL_RESPONSE" | jq -r '.overlay_ip')
    WG_CONFIG=$(echo "$ENROLL_RESPONSE" | jq -r '.wg_config')
    NETWORK_TYPE=$(echo "$ENROLL_RESPONSE" | jq -r '.network_type // "overlay"')
    # Device protocol v2 secret. Empty when brain is still v1-only.
    DEVICE_SECRET=$(echo "$ENROLL_RESPONSE" | jq -r '.device_secret // empty')

    echo "$NODE_ID" > "$NODE_ID_FILE"
    chmod 600 "$NODE_ID_FILE"
    echo "$BRAIN_URL" > "${KEY_DIR}/brain_url"
    chmod 600 "${KEY_DIR}/brain_url"
    if [[ -n "$DEVICE_SECRET" ]]; then
        printf '%s' "$DEVICE_SECRET" > "$DEVICE_SECRET_FILE"
        chmod 600 "$DEVICE_SECRET_FILE"
    fi
    # Bare overlay IP (no /prefix) — used by the heartbeat's same-LAN
    # state machine to alias the overlay /32 on the physical interface.
    echo "${OVERLAY_IP%/*}" > "${KEY_DIR}/overlay_ip"
    chmod 600 "${KEY_DIR}/overlay_ip"

    # ── macOS config post-processing ─────────────────────────────────────────
    #
    # 1. Strip Linux-specific PostUp/PreDown lines (reference /proc or `ip route`).
    #    These will cause wg-quick to fail on macOS.
    #
    # 2. For host role: the brain generates Address = x.x.x.x/NN (gateway subnet prefix).
    #    On macOS, that competing /24 route conflicts with the physical Ethernet interface
    #    already on the same subnet. We rewrite to /32 so only a host route is added,
    #    then enable IP forwarding via a macOS-safe PostUp.
    #
    # 3. For client role: Address is already /32 from the brain — no change needed.

    # Strip Linux PostUp/PreDown
    PROCESSED_CONFIG=$(echo "$WG_CONFIG" | grep -v '^PostUp' | grep -v '^PreDown')

    if [[ "$ROLE" == "host" && "$NETWORK_TYPE" == "native" ]]; then
        log "Native host mode: rewriting Address to /32 and adding macOS IP forwarding + NAT PostUp."

        # Extract the gateway subnet from the original Address line BEFORE rewriting to /32.
        # e.g. "Address = 192.168.1.2/24"  →  GW_SUBNET="192.168.1.0/24"
        ORIG_ADDR=$(echo "$PROCESSED_CONFIG" | awk '/^Address[[:space:]]*=/{print $3}')
        GW_SUBNET=$(python3 -c "import ipaddress; print(str(ipaddress.IPv4Network('${ORIG_ADDR}', strict=False)))" 2>/dev/null || echo "")
        [[ -z "$GW_SUBNET" ]] && die "Could not determine gateway subnet from Address '${ORIG_ADDR}'."
        log "Gateway subnet: ${GW_SUBNET}"

        # Rewrite /NN → /32 on the Address line (avoids competing route with physical LAN)
        PROCESSED_CONFIG=$(echo "$PROCESSED_CONFIG" | \
            sed 's/^\(Address = [0-9][0-9.]*\)\/[0-9]*/\1\/32/')

        # Write PostUp script: IP forwarding + pfctl NAT so LAN devices can reply to tunnel clients.
        # net.inet.proxyarp does not exist on modern macOS; NAT is the reliable alternative.
        cat > "$POSTUP_SCRIPT" << POSTUPEOF
#!/bin/bash
sysctl -w net.inet.ip.forwarding=1
PHYS=\$(route get default | awk '/interface:/{print \$2}')
printf "nat on %s from ${GW_SUBNET} to any -> (%s)\npass all\n" "\$PHYS" "\$PHYS" | pfctl -f - -e 2>/dev/null || true
POSTUPEOF
        chmod +x "$POSTUP_SCRIPT"

        # Write PreDown script: remove NAT rules when interface goes down.
        cat > "$PREDOWN_SCRIPT" << PREDOWNEOF
#!/bin/bash
pfctl -F nat 2>/dev/null || true
PREDOWNEOF
        chmod +x "$PREDOWN_SCRIPT"

        # Insert PostUp/PreDown references into the config after ListenPort line
        PROCESSED_CONFIG=$(echo "$PROCESSED_CONFIG" | \
            awk -v postup="$POSTUP_SCRIPT" -v predown="$PREDOWN_SCRIPT" \
            '/^ListenPort/{print; print "PostUp = " postup; print "PreDown = " predown; next}1')
    fi

    # Substitute private key placeholder
    PROCESSED_CONFIG=$(echo "$PROCESSED_CONFIG" | \
        sed "s|# PrivateKey = <CONNECTOR_FILLS_THIS_IN>|PrivateKey = ${PRIV_KEY}|")

    # Write config
    mkdir -p "$(dirname "$WG_CONF")"
    echo "$PROCESSED_CONFIG" > "$WG_CONF"
    chmod 600 "$WG_CONF"

    log "Enrolled! Assigned IP: ${OVERLAY_IP} | Node ID: ${NODE_ID} | Network type: ${NETWORK_TYPE}"
fi

NODE_ID=$(cat "$NODE_ID_FILE")

